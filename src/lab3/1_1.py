# a) Добавляем элемент 4 во второй элемент кортежа
tup = (1, [2, 3])
tup[1].append(4)
print("1a:", tup)

# b) К кортежу добавляем список из трех элементов
tup = (1, [2, 3])
tup = tup + ([5,6,7],)
print("1b:", tup)

# c) Добавляем строку
tup = (1, [2, 3])
tup = tup + ("hello",)
print("1c:", tup)

# d) Повторяем список во втором элементе
tup = (1, [2, 3])
tup = (tup[0], tup[1]*2)
print("1d:", tup)
